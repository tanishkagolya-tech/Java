public class comparisionoperator {
    public static void main(String[] args) {
        int a = 34;
        int b = 66;
        System.out.println("a==b : " + (a == b));
        System.out.println("a!=b : " + (a != b));
        System.out.println("a<b : " + (a < b));
        System.out.println("a>b : " + (a > b));
        System.out.println("a<=b : " + (a <= b));
        System.out.println("a>=b : " + (a >= b));
    }
}
