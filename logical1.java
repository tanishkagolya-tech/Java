public class logical1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        
        System.out.println("(a > 5 && b > 15) : " + (a > 5 && b > 15));
        System.out.println("(a > 15 || b > 15) : " + (a > 15 || b > 15));
        System.out.println("!(a > 5) : " + !(a > 5));
    }
}
    
    

